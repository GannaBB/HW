#Да се напише програма, която принтира редицата на Фибуначи между 0 и 50.
#Редицата на Фибуначи е: 0, 1, 1, 2, 3, 5, 8, 13, 21, … Всяко следващо число е сумата от
#предходните две.
#Изход: 1 1 2 3 5 8 13 21 34


N = 50

x = 1
y = 1
print(x,y, end=' ')

while x<N:
    
    tmp = x
    x = x + y
    y = tmp

    if x>=N:
        continue

    print(x, end=' ')