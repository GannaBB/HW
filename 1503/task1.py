#Да се напише програма, която да намира тези числа, които се делят на 7 и на 5, между 1500 и 2700.

print('Делят на 5: ')
for x in range(1500, 2701):
    if not x%5:
        print(x, end=',')


print(f"\n \n {'Делят на 7: '}")
      
for x in range(1500, 2701):
    if not x%7:
        print(x, end=',')